import 'package:flutter/material.dart';

class CustomThemeData {
  Color buttonEnableColor;
  Color buttonDisableColor;

  Color primaryColor;
  Color secondaryColor;
  Color thirdColor;
  Color loaderColor;
  Color errorColor;
  Color validColor;
  Color titleTextColor;
  Color subTitleTextColor;
  Color backgroundLightColor;
  Color backgroundGreyColor;

  CustomThemeData({
    required this.buttonEnableColor,
    required this.buttonDisableColor,
    required this.primaryColor,
    required this.secondaryColor,
    required this.thirdColor,
    required this.loaderColor,
    required this.errorColor,
    required this.validColor,
    required this.titleTextColor,
    required this.subTitleTextColor,
    required this.backgroundLightColor,
    required this.backgroundGreyColor,
  });
}
